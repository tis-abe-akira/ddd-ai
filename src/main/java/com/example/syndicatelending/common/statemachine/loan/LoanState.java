package com.example.syndicatelending.common.statemachine.loan;

/**
 * Loan（貸付）の状態を表すenum
 * 
 * シンジケートローンにおける貸付のライフサイクル管理を実現する。
 * 返済状況に応じた適切な状態管理により、ビジネスルールを強制する。
 */
public enum LoanState {
    /**
     * 作成直後状態（ドローダウン直後）
     * - 返済未開始
     * - 支払いスケジュール生成済み
     */
    DRAFT,
    
    /**
     * 返済中状態（正常返済中）
     * - 初回返済実行後
     * - 期日通りの返済が続いている状態
     */
    ACTIVE,
    
    /**
     * 遅延状態（返済遅延中）
     * - 返済期日を過ぎても未返済
     * - 延滞管理が必要な状態
     */
    OVERDUE,
    
    /**
     * 完済状態（返済完了）
     * - 全ての元本・利息返済完了
     * - 残高ゼロ
     */
    COMPLETED
}