package com.example.syndicatelending.fee.entity;

/**
 * 手数料タイプを表すenum
 * 
 * シンジケートローンにおける各種手数料の分類を定義し、
 * 手数料計算・配分ルールの判定に使用する。
 */
public enum FeeType {
    /**
     * 管理手数料
     * - リードバンクが受取る施設管理手数料
     * - 定期的（月次・四半期）に発生
     * - 融資枠残高に基づく固定料率
     */
    MANAGEMENT_FEE,
    
    /**
     * アレンジメント手数料
     * - シンジケート組成時の一時手数料
     * - リードバンクの組成作業に対する報酬
     * - 融資枠設定額に基づく
     */
    ARRANGEMENT_FEE,
    
    /**
     * コミットメント手数料
     * - 未使用融資枠に対する手数料
     * - 借り手が投資家に支払う
     * - 未使用残高×料率で計算
     */
    COMMITMENT_FEE,
    
    /**
     * 取引手数料
     * - ドローダウン・返済時の事務手数料
     * - 取引件数または金額ベース
     * - エージェントバンクが処理
     */
    TRANSACTION_FEE,
    
    /**
     * 遅延手数料
     * - 返済遅延時のペナルティ手数料
     * - 遅延日数×遅延金額×料率
     * - 投資家配分または運営費
     */
    LATE_FEE,
    
    /**
     * エージェント手数料
     * - エージェントバンクの事務代行手数料
     * - 資金決済・レポート作成等
     * - 定額または料率ベース
     */
    AGENT_FEE,
    
    /**
     * その他手数料
     * - 上記に分類されない各種手数料
     * - 契約変更手数料、証明書発行手数料等
     * - 個別設定
     */
    OTHER_FEE
}